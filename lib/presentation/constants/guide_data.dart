## 3. Viewing Files
1. Tap on any file inside a vault to decrypt and view it temporarily.
2. BlindKey supports images, videos, audio, PDF, and more.
3. To share a file securely, use the export option.

## 4. App Lock
1. Go to **Settings > Security**.
2. Enable **App Lock** to require a PIN when opening BlindKey.
3. This adds an extra layer of protection to your local data.

## 5. Exporting & Sharing
1. You can export a whole vault as a single `.blindkey` file.
2. Share this file with another BlindKey user.
3. They will need the password you set to unlock and view the contents.

